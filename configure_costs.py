def set_premium_mode():
    """Enable premium mode"""
    print("ğŸ”§ Configuring Premium Mode...")
    
    env_vars = {
        'USE_LOCAL_SENTIMENT': 'false',
        'SENTIMENT_CACHE_HOURS': '1',   # Cache for 1 hour only
        'REDUCE_API_CALLS': 'false'
    }
    
    update_env_file(env_vars)
    
    print("âœ… Premium Mode Enabled:")
    print("   â€¢ OpenAI GPT-4o-mini sentiment analysis")
    print("   â€¢ 1-hour sentiment caching")
